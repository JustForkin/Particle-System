#include "Object.h"

///////////////////////////////////////////////////////////////////
// constructor
//
///////////////////////////////////////////////////////////////////
Object::Object(int id) {

	this->_transformable = true;

	this->_selectable = true;

	this->_id = id;
}

///////////////////////////////////////////////////////////////////
//
//
///////////////////////////////////////////////////////////////////
int Object::getId() {

	return _id;
}

///////////////////////////////////////////////////////////////////
//
//
///////////////////////////////////////////////////////////////////
Vector* Object::getPosition() {

	return &_position;
}

///////////////////////////////////////////////////////////////////
//
//
///////////////////////////////////////////////////////////////////
Vector* Object::getOffset() {

	return &_offset;
}
